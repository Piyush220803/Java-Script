const month = "april"  // or there can be numeric value also

switch (month) {
    case "jan":
        console.log("jan hai bhai");
        
        break;
    case "feb":
        console.log("feb hai bhai");
        
        break;
    case "march":
        console.log("march hai bhai");
        
        break;
    case "april":
        console.log("april hai bhai");
        
        break;

    default:
        console.log(`kuch bhi`);
        
        break;
}